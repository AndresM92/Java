    //BOTON ELIMINAR ESTUDIANTE
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String doc=JOptionPane.showInputDialog("Documento de estudiante a Eliminar");
        String confirmacion=JOptionPane.showInputDialog("Â¿Esta seguro?  Y/N");
        if(confirmacion.toUpperCase().compareTo("Y")==0)
        for(int i=0; i<estudiantes.size();i++){
            if(doc.compareTo(estudiantes.get(i).getDoc())==0){
                estudiantes.remove(i);
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    
