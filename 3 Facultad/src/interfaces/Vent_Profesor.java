    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String doc=JOptionPane.showInputDialog("Documento de Profesor a cambiar estado civil");
        String estado=JOptionPane.showInputDialog("Nuevo Estado Civil");
        String confirmacion=JOptionPane.showInputDialog("多Esta seguro?  Y/N");
        if(confirmacion.toUpperCase().compareTo("Y")==0){
            for(int i=0; i<profesores.size();i++){
                if(doc.compareTo(profesores.get(i).getDoc())==0){
                    if(profesores.get(i).getEstado_civil().toUpperCase().compareTo("CASADO")==0){
                        double p=profesores.get(i).getSalario() - profesores.get(i).getSalario()*0.08;
                        profesores.get(i).setSalario(p);
                    }
                    profesores.get(i).setEstado_civil(estado);                 
                }
            }
        }    
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String doc=JOptionPane.showInputDialog("Documento de Profesor a Cambiar Despacho");
        String despacho=JOptionPane.showInputDialog("Nuevo Despacho");
        String confirmacion=JOptionPane.showInputDialog("多Esta seguro?  Y/N");
        if(confirmacion.toUpperCase().compareTo("Y")==0){
            for(int i=0; i<profesores.size();i++){
                if(doc.compareTo(profesores.get(i).getDoc())==0){
                    profesores.get(i).reasignar_despacho(despacho);
                }
            }
        
        }  
    }//GEN-LAST:event_jButton6ActionPerformed

