    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        for(int i=0; i<estudiantes.size(); i++){
            if(jTextField1.getText().compareTo(estudiantes.get(i).getDoc())==0){
                jTextField2.setText(estudiantes.get(i).getNombre());
                jTextField3.setText(estudiantes.get(i).getApellido());
                jTextField4.setText(estudiantes.get(i).getEstado_civil());
                jTextField5.setText(estudiantes.get(i).getCurso());
            }
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

