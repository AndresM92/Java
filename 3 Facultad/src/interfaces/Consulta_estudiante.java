    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        for(int i=0; i<estudiantes.size(); i++){
            if(jTextField1.getText().compareTo(estudiantes.get(i).getDoc())==0){
                estudiantes.get(i).setNombre(jTextField2.getText());
                estudiantes.get(i).setApellido(jTextField3.getText());
                estudiantes.get(i).setEstado_civil(jTextField4.getText());
                estudiantes.get(i).setCurso(jTextField5.getText());
            }
        }    
        
    }//GEN-LAST:event_jButton2ActionPerformed

