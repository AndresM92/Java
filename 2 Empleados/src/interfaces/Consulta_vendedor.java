    //BOTON CONSULTAR VENDEDOR
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        for(int i=0; i<vendedores.size(); i++){
            if(jTextField1.getText().compareTo(vendedores.get(i).getDoc())==0){
                jTextField2.setText(vendedores.get(i).getNombre());
                jTextField3.setText(vendedores.get(i).getApellido());
                jTextField4.setText(vendedores.get(i).getDireccion());
                jTextField5.setText(vendedores.get(i).getAnios_antiguedad());
                jTextField6.setText(vendedores.get(i).getTelefono());
                jTextField7.setText(String.valueOf(vendedores.get(i).getSalario()));
                jTextField8.setText(vendedores.get(i).getTelefono_movil());
                jTextField9.setText(vendedores.get(i).getArea_venta()); 
                jTextField10.setText(vendedores.get(i).getMatricula()); 
                jTextField11.setText(vendedores.get(i).getMarca()); 
                jTextField12.setText(vendedores.get(i).getModelo());
                ArrayList clientes=new ArrayList<>();
                clientes=vendedores.get(i).getClientes();
                for(int j=0; j<clientes.size();j++){
                modeloLista.addElement(clientes.get(j));
                }
            }
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

