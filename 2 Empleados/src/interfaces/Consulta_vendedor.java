    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        for(int i=0; i<vendedores.size(); i++){
            if(jTextField1.getText().compareTo(vendedores.get(i).getDoc())==0){
                vendedores.get(i).setNombre(jTextField2.getText());
                vendedores.get(i).setApellido(jTextField3.getText());
                vendedores.get(i).setDireccion(jTextField4.getText());
                vendedores.get(i).setAnios_antiguedad(jTextField5.getText());
                vendedores.get(i).setTelefono(jTextField6.getText());
                vendedores.get(i).setSalario(Double.parseDouble(jTextField7.getText()));
                vendedores.get(i).setTelefono_movil(jTextField8.getText());
                vendedores.get(i).setArea_venta(jTextField9.getText()); 
                vendedores.get(i).setMatricula(jTextField10.getText()); 
                vendedores.get(i).setMarca(jTextField11.getText()); 
                vendedores.get(i).setModelo(jTextField12.getText()); 
            }
        }
        
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

