    //BOTON BUSCAR
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        for(int i=0; i<secretarios.size(); i++){
            if(jTextField1.getText().compareTo(secretarios.get(i).getDoc())==0){
                jTextField2.setText(secretarios.get(i).getNombre());
                jTextField3.setText(secretarios.get(i).getApellido());
                jTextField4.setText(secretarios.get(i).getDireccion());
                jTextField5.setText(secretarios.get(i).getAnios_antiguedad());
                jTextField6.setText(secretarios.get(i).getTelefono());
                jTextField7.setText(String.valueOf(secretarios.get(i).getSalario()));
                jTextField8.setText(secretarios.get(i).getDespacho());
                jTextField9.setText(secretarios.get(i).getFax()); 
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

