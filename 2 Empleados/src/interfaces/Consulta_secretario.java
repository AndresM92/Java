    //BOTON GUARDAR
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        for(int i=0; i<secretarios.size(); i++){
            if(jTextField1.getText().compareTo(secretarios.get(i).getDoc())==0){
                secretarios.get(i).setNombre(jTextField2.getText());
                secretarios.get(i).setApellido(jTextField3.getText());
                secretarios.get(i).setDireccion(jTextField4.getText());
                secretarios.get(i).setAnios_antiguedad(jTextField5.getText());
                secretarios.get(i).setTelefono(jTextField6.getText());
                secretarios.get(i).setSalario(Double.parseDouble(jTextField7.getText()));
                secretarios.get(i).setDespacho(jTextField8.getText());
                secretarios.get(i).setFax(jTextField9.getText()); 
            }
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

