    //CAMBIAR AUTOMOVIL
    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
        String documento=JOptionPane.showInputDialog("Documento");     
        String matricula=JOptionPane.showInputDialog("Nueva Matricula Carro");
        String marca=JOptionPane.showInputDialog("Nueva Marca de Carro");
        String modelo=JOptionPane.showInputDialog("Nuevo Modelo Carro");
        
        for(int i=0; i<vendedores.size(); i++){
            if(documento.compareTo(vendedores.get(i).getDoc())==0){
                vendedores.get(i).setMatricula(matricula);
                vendedores.get(i).setMarca(marca);
                vendedores.get(i).setModelo(modelo);
            }
        }
        
        
        
    }//GEN-LAST:event_jButton6ActionPerformed

    
    //BOTON AGREGAR CLIENTE
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String documento=JOptionPane.showInputDialog("Documento de Vendedor");
        String cantidad=JOptionPane.showInputDialog("Cantidad de clientes a Guardar");   
        int cant= Integer.parseInt(cantidad);
        
        for(int j=0; j<vendedores.size();j++){
            if(documento.compareTo(vendedores.get(j).getDoc())==0){
                for(int i=0;i<cant; i++){
                    String cliente=JOptionPane.showInputDialog("Nuevo cliente");
                    vendedores.get(j).Insertar_cliente(cliente);
                }
        
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    
    //ELIMINAR CLIENTE
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String documento=JOptionPane.showInputDialog("Documento de Vendedor");
        String cliente=JOptionPane.showInputDialog("Cliente a Eliminar");
        for(int j=0; j<vendedores.size();j++){
            if(documento.compareTo(vendedores.get(j).getDoc())==0){
                vendedores.get(j).eliminar_cliente(cliente);
                }
            }
    }//GEN-LAST:event_jButton5ActionPerformed

