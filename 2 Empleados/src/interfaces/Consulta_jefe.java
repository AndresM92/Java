    //BOTON CONSULTAR
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        for(int i=0; i<jefes.size(); i++){
            if(jTextField1.getText().compareTo(jefes.get(i).getDoc())==0){
                jTextField2.setText(jefes.get(i).getNombre());
                jTextField3.setText(jefes.get(i).getApellido());
                jTextField4.setText(jefes.get(i).getDireccion());
                jTextField5.setText(jefes.get(i).getAnios_antiguedad());
                jTextField6.setText(jefes.get(i).getTelefono());
                jTextField7.setText(String.valueOf(jefes.get(i).getSalario()));
                if(jefes.get(i).getSecretario().getNombre().compareTo("")==0){
                    jTextField8.setText("");
                    jTextField9.setText("");
                    jTextField10.setText("");
                }else{
                    jTextField8.setText(jefes.get(i).getSecretario().getNombre());
                    jTextField9.setText(jefes.get(i).getSecretario().getApellido()); 
                    jTextField10.setText(jefes.get(i).getSecretario().getDoc());                 
                }
                jTextField11.setText(jefes.get(i).getMatricula()); 
                jTextField12.setText(jefes.get(i).getMarca()); 
                jTextField13.setText(jefes.get(i).getModelo());
                
                ArrayList<Vendedor> vendedores=new ArrayList<>();
                vendedores=jefes.get(i).getVendedores();
                for(int j=0; j<vendedores.size();j++){
                    modeloLista.addElement(vendedores.get(j).getNombre()+vendedores.get(j).getApellido());
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    
    //BOTON GUARDAR
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        for(int i=0; i<jefes.size(); i++){
            if(jTextField1.getText().compareTo(jefes.get(i).getDoc())==0){
                jefes.get(i).setNombre(jTextField2.getText());
                jefes.get(i).setApellido(jTextField3.getText());
                jefes.get(i).setDireccion(jTextField4.getText());
                jefes.get(i).setAnios_antiguedad(jTextField5.getText());
                jefes.get(i).setTelefono(jTextField6.getText());
                jefes.get(i).setSalario(Double.parseDouble(jTextField7.getText()));
                jefes.get(i).setMatricula(jTextField11.getText()); 
                jefes.get(i).setMarca(jTextField12.getText()); 
                jefes.get(i).setModelo(jTextField13.getText()); 
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

